/**
 * \file todo-groups.h
 * \brief Doxygen ToDo category group definitions
 * \author LCDproc-G15 Team
 * \date 2025
 *
 * \details This file defines the ToDo priority groups used throughout the codebase
 * for organizing ToDos by priority in the generated documentation.
 */

/**
 * \defgroup ToDo_critical Critical Priority ToDos
 * \brief High-priority issues requiring immediate attention
 *
 * Critical issues affecting thread-safety, core functionality, or system stability.
 */

/**
 * \defgroup ToDo_medium Medium Priority ToDos
 * \brief Medium-priority improvements and missing features
 *
 * Feature enhancements and improvements that affect user experience or code quality.
 */

/**
 * \defgroup ToDo_low Low Priority ToDos
 * \brief Low-priority enhancements and nice-to-have features
 *
 * Minor improvements, additional API functions, and test coverage.
 */
